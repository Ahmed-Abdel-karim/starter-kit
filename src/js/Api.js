export const getProducts = (category, page, pageSize) => {
  const currentIndex = parseInt(page - 1) * parseInt(pageSize);

  if (category === 'all') {
    return {
      total: products.length,
      list: products.map(({ sale, ...other }) => ({ ...other })).slice(currentIndex, currentIndex + pageSize)
    };
  }

  const matchedProducts = products.filter(i => i.category === category).map(({ sale, ...other }) => ({ ...other }));
  return {
    total: matchedProducts.length,
    list: matchedProducts.slice(currentIndex, currentIndex + pageSize)
  };
};

